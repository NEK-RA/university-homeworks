package nek.java.lab1.task1;

public class Dog {
  private String name;
  private float age;

  public Dog(String name, float age){
    this.name = name;
    this.age = age;
  }

  public String getName() {
    return name;
  }

  
  public void setName(String name) {
    this.name = name;
  }

  
  public float getAge() {
    return age;
  }

  
  public void setAge(float age) {
    this.age = age;
  }

}